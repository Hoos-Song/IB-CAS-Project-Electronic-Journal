    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            String create = null;
            if (rs.getInt("creativity") == 0) {
                create = "no";
            } else {
                create = "yes";
            }
            String act = null;
            if (rs.getInt("action") == 0) {
                act = "no";
            } else {
                act = "yes";
            }
            String serve = null;
            if (rs.getInt("service") == 0) {
                serve = "no";
            } else { 
                serve = "yes";
            }
            String id = rs.getString("studentID").toUpperCase().trim();
            String[] ID = new String[1];
            ID[0] = id;
            fileSaver.main(ID);
            fileSaver writer = new fileSaver();
            writer.openFileForWriteAppend("Name: " + rs.getString("firstName").trim() + " " +
                    rs.getString("lastName").trim() + "\n" +
                    "ID: " + rs.getString("studentID").trim() + "\n" +
                    "Start Date: " + rs.getString("startDate").trim() + "\n" +
                    "End Date: " + rs.getString("endDate").trim() + "\n" +
                    "Project Name: " + rs.getString("projectName").trim() + "\n" +
                    "Creativity: " + create + "\n" +
                    "Action " + act + "\n" +
                    "Service: " + serve + "\n" + 
                    "Objective: " + rs.getString("objective").trim() + "\n" +
                    "Hours: " + rs.getString("hours").trim() + "\n" +
                    "Reflection: " + rs.getString("reflection").trim() + "\n" + "\n");
            
            
            JOptionPane.showMessageDialog(this, "Be sure to open the file 'casreport" + id + ".txt' in a Word Document for correct formatting");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

