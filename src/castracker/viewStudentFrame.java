    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here: select * from dbo.something where 
        String studentID = txtStudentID.getText();
        String lastNameyeah = txtLastName.getText();  
        try {
            String host = "jdbc:sqlserver://localhost:1433;databaseName=master;integratedSecurity = true";
            con = DriverManager.getConnection(host);
            stmt = con.createStatement();
            rs = stmt.executeQuery("Select * from dbo.CASpasswordID where lastName = '" +
                    lastNameyeah + "' and ID = '" + studentID + "'");
            
            
            
            DefaultListModel table = new DefaultListModel();
            while (rs.next()) {
                String output = rs.getString("firstName").trim() + " " + rs.getString("lastName").trim() + "  ID: " + rs.getString("ID").trim();
                table.addElement(output);
            }
            lstResults.setModel(table);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,"error 5 "+ ex.getMessage());
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

