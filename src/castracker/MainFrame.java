    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        this.checkTextIsEmpty();
        System.out.println(btnLogOut.getBounds());
        if (isTextEmpty() && !cbbObjectives.getSelectedItem().toString().equals("Objective") && (cboCreativity.isSelected()
                || cboAction.isSelected() || cboService.isSelected())) {
            System.out.println(lblLastName.getText() + ", " + lblFirstName.getText());

            try {
                Connection con;
                String host = "jdbc:sqlserver://localhost:1433;databaseName=master;integratedSecurity = true";
                con = DriverManager.getConnection(host);
                stmt = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);

                /**
                 * execute a query where you can get the specific record for the
                 * studentID. Once you have the record you must put it into
                 * storage variables Now update each storage variable with the
                 * new information You will have to concatenate the new data
                 * with the old data. Therefore: if no record exists for the
                 * specified studentID then simply update the information with
                 * the new. If the record exists add each new element to each
                 * old element and update the information.
                 */
                rs = stmt.executeQuery("SELECT COUNT (*) FROM dbo.CAStable WHERE studentID = '" + lblID.getText() + "'");
                rs.next();
                cs = con.prepareCall("{call setStudentInfo(?,?,?,?,?,?,?,?,?,?,?,?)}");
                String b = lblLastName.getText();
                cs.setString(1, b);
                String l = lblFirstName.getText();
                cs.setString(2, l);
                cs.setString(3, lblDate1.getText());
                cs.setString(4, lblDate.getText());
                cs.setString(5, txtProjectName.getText());

                if (cboCreativity.isSelected()) {
                    cs.setInt(6, 1);
                } else {
                    cs.setInt(6, 0);
                }
                if (cboAction.isSelected()) {
                    cs.setInt(7, 1);
                } else {
                    cs.setInt(7, 0);
                }
                if (cboService.isSelected()) {
                    cs.setInt(8, 1);
                } else {
                    cs.setInt(8, 0);
                }
                cs.setString(9, cbbObjectives.getSelectedItem().toString());
                String j = txtTime.getText();
                int k = Integer.parseInt(j);
                String time = k + "hours";
                cs.setString(10, time);
                cs.setString(11, txtReflection.getText());
                String m = lblID.getText();
                cs.setString(12, m);
                cs.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data Entered");
                //}
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error 1: ");
                System.exit(0);
            }

        }
    }//GEN-LAST:event_btnSubmitActionPerformed
    private class MyDocFilter extends DocumentFilter {

        @Override
        public void insertString(DocumentFilter.FilterBypass fb, int offset,
                String text, AttributeSet attr) throws BadLocationException {
            for (char c : text.toCharArray()) {
                if (!Character.isDigit(c)) {
                    return;
                }
            }
            fb.insertString(offset, text.toUpperCase(), attr);
        }

        @Override
        public void replace(DocumentFilter.FilterBypass fb, int offset,
                int length, String text, AttributeSet attrs)
                throws BadLocationException {
            for (char c : text.toCharArray()) {
                if (!Character.isDigit(c)) {
                    return;
                }
            }
            fb.replace(offset, length, text.toUpperCase(), attrs);
        }
    }
