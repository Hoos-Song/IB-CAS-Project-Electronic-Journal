    private void checkTextIsEmpty() {

        if (lblDate.getText().isEmpty() || lblDate1.getText().isEmpty()) {
            lblWarn4.setVisible(true);
        } else {
            lblWarn4.setVisible(false);
        }

        if (txtProjectName.getText().isEmpty()) {
            lblWarn1.setVisible(true);
        } else {
            lblWarn1.setVisible(false);
        }

        if (txtTime.getText().isEmpty()) {
            lblWarn2.setVisible(true);
        } else {
            lblWarn2.setVisible(false);
        }
        if (txtReflection.getText().isEmpty()) {
            lblWarn3.setVisible(true);
        } else {
            lblWarn3.setVisible(false);
        }
    }

    private boolean isTextEmpty() {

        if (lblDate.getText().isEmpty() || lblDate1.getText().isEmpty()) {
            return false;
        } else if (!lblDate.getText().isEmpty() && !lblDate1.getText().isEmpty()) {
            return true;
        } else if (txtProjectName.getText().isEmpty()) {
            return false;
        } else if (!txtProjectName.getText().isEmpty()) {
            return true;
        } else if (txtTime.getText().isEmpty()) {
            return false;
        } else if (!txtTime.getText().isEmpty()) {
            return true;
        } else if (txtReflection.getText().isEmpty()) {
            return false;
        } else if (!txtReflection.getText().isEmpty()) {
            return true;
        } else {
            return true;
        }

    }

